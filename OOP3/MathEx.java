import java.lang.Math;

public class MathEx {
  public static void main(String[] args) {
    System.out.println("MathEx");
    System.out.println("PI: " + Math.PI);
    System.out.println("E: " + Math.E);
    System.out.println("Square root of 16: " + Math.round(16.57));
    System.out.println("Square root of 16: " + Math.ceil(16.4));
    System.out.println("Square root of 16: " + Math.floor(16.4));
    System.out.println("Square root of 16: " + Math.sqrt(16));
    System.out.println("Power of 2^3: " + Math.pow(2, 3));
    System.out.println("Random number: " + Math.random());
  }
}
